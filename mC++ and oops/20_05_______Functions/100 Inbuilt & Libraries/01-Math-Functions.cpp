#include <iostream>
#include <cmath> // for math functions
using namespace std;

int main() {
    cout << "pow(2, 3) = " << pow(2, 3) << endl;       // 2^3 = 8
    cout << "sqrt(25) = " << sqrt(25) << endl;         // square root
    cout << "cbrt(27) = " << cbrt(27) << endl;         // cube root
    cout << "abs(-10) = " << abs(-10) << endl;         // absolute value
    cout << "ceil(4.3) = " << ceil(4.3) << endl;       // round up
    cout << "floor(4.7) = " << floor(4.7) << endl;     // round down
    cout << "round(4.5) = " << round(4.5) << endl;     // round nearest
    cout << "fmax(3, 7) = " << fmax(3, 7) << endl;     // max of two
    cout << "fmin(3, 7) = " << fmin(3, 7) << endl;     // min of two
}
