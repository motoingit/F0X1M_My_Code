#include <iostream>
#include <string>
using namespace std;
int main() {
    string a = "Hello";
    string b = "C++";
    string c = "";
    cout << "Length examples:" << endl;
    cout << "a: '" << a << "' length = " << a.length() << endl;
    cout << "b: '" << b << "' length = " << b.length() << endl;
    cout << "c: '" << c << "' length = " << c.length() << endl;
    return 0;
}
