#include <stdio.h>
int main(){
    int n = 10;
    int str[] = {1,2,3,4,5,6,7,8,9,0};
    int k = 7;

    int i,j;
    for(i = 0; i < n - 1; i++){
        for( j = 1 + i; j < n; j++){
            if(str[i] + str[j] == k){
                printf("Index [%d]->[%d] + [%d]->[%d] = [%d]\n",i,str[i],j,str[j],k);
            }
        }
    }
    
return 0;}

